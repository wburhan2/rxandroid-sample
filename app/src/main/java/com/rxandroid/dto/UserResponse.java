public class UserResponse {
	int id;
	String name;
	String email;
	String phone;
	String username;

	public String getEmail() {
		return email;
	}

	public int getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public String getPhone() {
		return phone;
	}

	public String getUsername() {
		return username;
	}
}
